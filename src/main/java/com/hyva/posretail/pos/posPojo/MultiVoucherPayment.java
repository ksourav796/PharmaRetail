package com.hyva.posretail.pos.posPojo;

import org.codehaus.jackson.annotate.JsonIgnoreProperties;

import java.util.Date;

/**
 * Created by chinmay.b on 3/10/2017.
 */
@JsonIgnoreProperties(ignoreUnknown = true)
public class MultiVoucherPayment {
    private String voucherNo;
    private double voucherAmt;
    private String voucherCustomerName;
    private Date VoucherExpiryDAte;
    private String voucherCode;

    public String getVoucherCustomerName() {
        return voucherCustomerName;
    }

    public void setVoucherCustomerName(String voucherCustomerName) {
        this.voucherCustomerName = voucherCustomerName;
    }

    public Date getVoucherExpiryDAte() {
        return VoucherExpiryDAte;
    }

    public void setVoucherExpiryDAte(Date voucherExpiryDAte) {
        VoucherExpiryDAte = voucherExpiryDAte;
    }

    public String getVoucherCode() {
        return voucherCode;
    }

    public void setVoucherCode(String voucherCode) {
        this.voucherCode = voucherCode;
    }

    public String getVoucherNo() {
        return voucherNo;
    }

    public void setVoucherNo(String voucherNo) {
        this.voucherNo = voucherNo;
    }

    public double getVoucherAmt() {
        return voucherAmt;
    }

    public void setVoucherAmt(double voucherAmt) {
        this.voucherAmt = voucherAmt;
    }
}
