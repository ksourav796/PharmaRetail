package com.hyva.posretail.pos.posPojo;

public class GiftCardDetails {
    public String giftCardNo;
    public double giftCardAmt;
    public double AmountUsed;

    public String getGiftCardNo() {
        return giftCardNo;
    }

    public void setGiftCardNo(String giftCardNo) {
        this.giftCardNo = giftCardNo;
    }

    public double getGiftCardAmt() {
        return giftCardAmt;
    }

    public void setGiftCardAmt(double giftCardAmt) {
        this.giftCardAmt = giftCardAmt;
    }

    public double getAmountUsed() {
        return AmountUsed;
    }

    public void setAmountUsed(double amountUsed) {
        AmountUsed = amountUsed;
    }
}
